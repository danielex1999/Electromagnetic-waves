    public ArrayList<Tabla> tablalistado() {
        ArrayList<Tabla> tablalistado = new ArrayList<>();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            String url = "jdbc:mysql://localhost:3306/dbwaveselectronic";
            String user = "root";
            String contra = "";
            Connection con = DriverManager.getConnection(url, user, contra);
            String queryfirst = "SELECT * FROM dispoelectronicos Order by modelo";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(queryfirst);
            Tabla tablita;

            while (rs.next()) {
                tablita = new Tabla(rs.getString("modelo"), rs.getString("diselec"), rs.getDouble("tresdis"), rs.getDouble("treintadis"), rs.getDouble("unmdis"), rs.getInt("campele"));
                tablalistado.add(tablita);
            }
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return tablalistado;
    }

    private void show_table() {
        ArrayList<Tabla> list = tablalistado();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        Object[] Row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            Row[0] = list.get(i).getModelo();
            Row[1] = list.get(i).getDiselec();
            Row[2] = list.get(i).getTresdis();
            Row[3] = list.get(i).getTreintadis();
            Row[4] = list.get(i).getUnmdis();
            Row[5] = list.get(i).getCampele();

            model.addRow(Row);
        }

    }

