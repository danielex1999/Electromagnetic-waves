    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        int val = jComboBox1.getSelectedIndex();
        switch (val) {
            case 0:
                tablalistado().clear();
                show_table();
                break;
            case 1:
                valor = "Secador de Pelo";
                busqueda().clear();
                show_search();
                break;
            case 2:
                valor = "Aspiradora";
                busqueda().clear();
                show_search();
                break;
            case 3:
                valor = "Microondas";
                busqueda().clear();
                show_search();
                break;
            case 4:
                valor = "Horno eléctrico";
                busqueda().clear();
                show_search();
                break;
            default:
                valor = "Ordenador";
                busqueda().clear();
                show_search();
                break;
        }


    }//GEN-LAST:event_jButton1ActionPerformed

